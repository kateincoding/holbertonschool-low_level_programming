#include "3-calc.h"

/**
 * op_add - sum
 * @a: number a
 * @b: number b
 * Return: result of operation
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts
 * @a: number a
 * @b: number b
 * Return: result of operation
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product
 * @a: number a
 * @b: number b
 * Return: result of operation
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division
 * @a: number a
 * @b: number b
 * Return: result of operation
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: number a
 * @b: number b
 * Return: result of operation
 */
int op_mod(int a, int b)
{
	return (a % b);
}
