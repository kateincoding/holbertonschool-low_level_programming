/**
 * add - add
 * @a: first number
 * @b: second number
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - sub
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - sub
 * @a: first number
 * @b: second number
 * Return: a - b
 */
int mul(int a, int b)
{
	return ((a) * (b));
}

/**
 * div - div
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - div
 * @a: first number
 * @b: second number
 * Return: a / b
 */
int mod(int a, int b)
{
	return (a % b);
}
